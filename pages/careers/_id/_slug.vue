<template>
  <careerSingle :career="career" />
</template>

<script>
import axios from 'axios'
export default {
  components: {
    careerSingle: () => import('~/components/Careers/CareerSingle'),
  },
  layout: 'default',
  asyncData(context) {
    return axios
      .get(process.env.baseUrl + '/careers/' + context.params.id)
      .then((res) => {
        return {
          career: res.data,
        }
      })
      .catch((e) => context.error(e))
  },
  head() {
    const i18nSeo = this.$nuxtI18nSeo()
    return {
      title:
        this.pageTitle +
        ' - ' +
        (this.$i18n.locale === 'ar'
          ? 'الجمعية الأردنية للمصدر المفتوح'
          : 'Jordan Open Source Association'),
      meta: [
        {
          name: 'description',
          content: this.career['metaDescription_' + this.$i18n.locale]
            ? this.career['metaDescription_' + this.$i18n.locale]
            : '',
        },
        ...this.$options.filters.ogTags(
          'career',
          this.career,
          this.$route.path,
          this.$i18n.locale
        ),
        ...i18nSeo.meta,
      ],
    }
  },
  computed: {
    pageTitle() {
      return this.career['title_' + this.$i18n.locale]
    },
  },
}
</script>
