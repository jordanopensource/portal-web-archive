<template>
  <EventSingle :event="event" />
</template>

<script>
import axios from 'axios'
export default {
  components: {
    EventSingle: () => import('~/components/Events/EventSingle'),
  },
  layout: 'default',
  asyncData(context) {
    return axios
      .get(process.env.baseUrl + '/events/' + context.params.id)
      .then((res) => {
        return {
          event: res.data,
        }
      })
      .catch((e) => context.error(e))
  },
  head() {
    const i18nSeo = this.$nuxtI18nHead({
      addDirAttribute: true,
      addSeoAttributes: true,
    })
    return {
      title:
        this.pageTitle +
        ' - ' +
        (this.$i18n.locale === 'ar'
          ? 'الجمعية الأردنية للمصدر المفتوح'
          : 'Jordan Open Source Association'),
      meta: [
        {
          name: 'description',
          content: this.event['metaDescription_' + this.$i18n.locale]
            ? this.event['metaDescription_' + this.$i18n.locale]
            : '',
        },
        ...this.$options.filters.ogTags(
          'event',
          this.event,
          this.$route.path,
          this.$i18n.locale
        ),
        ...i18nSeo.meta,
      ],
    }
  },
  computed: {
    pageTitle() {
      return this.event['title_' + this.$i18n.locale]
    },
  },
}
</script>
